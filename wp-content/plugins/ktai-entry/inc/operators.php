class KtaiEntry_Operator {
	public $base; // needs public when called by hook
	public $pictogram_type;

/* ==================================================
 * @param	string   $sender
 * @param	array    $recipients
 * @return	object   $this
 */
public static function factory($sender, $recipients) {
	$operator = NULL;
	$is_yahoojp = false;

	foreach ( $recipients as $r) {
		if ( is_email($r) && preg_match('/@(yahoo\.co|ybb\.ne)\.jp$/i', $r) ) {
			$is_yahoojp = true;
			break;
		}
	}
	if ( is_email($sender) ) {
		if (preg_match('/@(ezweb\.ne|auone)\.jp$/i', $sender)) {
			$operator = new KtaiEntry_EZweb();
			add_filter('ktai_checked_mime_text', array($operator, 'pickup_pictograms'), 10, 2);
		} elseif (preg_match('/@((\w+\.)?pdx\.ne\.jp|willcom\.com)$/i', $sender)) {
			$operator = new KtaiEntry_WILLCOM();
			add_filter('ktai_raw_mime_text', array($operator, 'pickup_pictograms'), 10, 2);
		} elseif (preg_match('/@docomo\.ne\.jp$/i', $sender)) {
			if ($is_yahoojp) {
				$operator = new KtaiEntry_imode_ISO();
				add_filter('ktai_raw_mime_text', array($operator, 'pickup_pictograms'), 10, 2);
			} else {
				$operator = new KtaiEntry_imode_SJIS();
				add_filter('ktai_checked_mime_text', array($operator, 'pickup_pictograms'), 10, 2);
			}
		} elseif (preg_match('/@emnet\.ne\.jp$/i', $sender)) {
			$operator = new KtaiEntry_EMNet();
			add_filter('ktai_checked_mime_text', array($operator, 'pickup_pictograms'), 10, 2);
		} elseif (preg_match('/@((softbank|disney|[dhrtcknsq]\.vodafone)\.ne|i\.softbank)\.jp$/i', $sender)) {
			if ($is_yahoojp) {
				$operator = new KtaiEntry_SoftBank_ISO();
				add_filter('ktai_raw_mime_text', array($operator, 'pickup_pictograms'), 10, 2);
			} else {
				$operator = new KtaiEntry_SoftBank_SJIS();
				add_filter('ktai_checked_mime_text', array($operator, 'pickup_pictograms'), 10, 2);
			}
		}
	}
	return $operator;
}

/* ==================================================
 * @param	none
 * @return	none
 */
public function __construct() {
	global $Ktai_Entry;
	$this->base = $Ktai_Entry;
	$this->pictogram_type = __('(N/A)', 'ktai_entry_log');
	return;
}

/* ==================================================
 * @param	string   $buffer
 * @param	string   $encoding
 * @return	string   $buffer
 */
protected function pickup_iso_pictograms($buffer, $encoding, $pictograms) {
	if ($encoding == 'auto') {
		$encoding = $this->base->encode->guess($buffer);
		$this->base->logging("Detect content encoding as '$encoding' to pickup pictgrams.");
	}
	if ( $this->base->encode->similar($encoding, 'ISO-2022-JP') ) {
		$this->base->debug_print(__('Pictogram encoding is ISO-2022-JP.', 'ktai_entry_log'));
		for ($offset = 0 , $replace = 'X' ;
			 preg_match('/\x1b\$B([\x21-\x7e]+)\x1b\(B/', $buffer, $sequence, PREG_OFFSET_CAPTURE, $offset); //)
			 $offset += strlen($replace)) 
		{
			$orig    = $sequence[0][0];
			$offset  = $sequence[0][1];
			$chars   = $sequence[1][0];
			$replace = "\x1b\$B";
			for ($i = 0 ; $i < strlen($chars) -1 ; $i += 2) {
				if (isset($pictograms[$chars[$i] . $chars[$i+1]])) {
					$replace .= sprintf("\x1b(B<img localsrc=\"%s\" />\x1b\$B", $pictograms[$chars[$i] . $chars[$i+1]]); //)
				} else {
					$replace .= $chars[$i] . $chars[$i+1];
				}
			}
			$replace .= "\x1b(B"; //)
			$replace = str_replace("\x1b\$B\x1b(B", '', $replace); //)
			$buffer = substr_replace($buffer, $replace, $offset, strlen($orig));	
		}
	}
	return $buffer;
}

/* ==================================================
 * @param	string   $buffer
 * @param	string   $encoding
 * @param	array    $pictograms
 * @return	string   $buffer
 */
protected function pickup_docomo_sjis($buffer, $encoding, $pictograms) {
	$GLOBALS['pictograms'] = $pictograms; // pass $pictograms to eval scope of mb_ereg_replace/preg_replace
	$replaced = $this->base->encode->replace(
		"([\xf8\x40-\xf8\x5b]|[\xf8\x5d-\xf8\xfc]|[\xf9\x40-\xf9\x5b]|[\xf9\x5d-\xf9\xfc])", 
		'isset($GLOBALS["pictograms"]["\1"]) ? 
		"<img localsrc=\"" . $GLOBALS["pictograms"]["\1"] . "\" />" : 
		"<img localsrc=\"d\" alt=\"" . "[0x" . bin2hex("\1") . "]\" />"', 
		$buffer, 'SJIS-win', 'e');
	if ($replaced) {
		$replaced = $this->base->encode->replace("\xf8\x5c", '<img localsrc="d" alt="[0xf85c]" />', $replaced, 'SJIS-win');
		$replaced = $this->base->encode->replace("\xf9\x5c", '<img localsrc="' . $pictograms["\xf9\x5c"] . '" />', $replaced, 'SJIS-win');
	}
	return $replaced ? $replaced : $buffer;
}

/* ==================================================
 * @param	string   $buffer
 * @param	string   $encoding
 * @param	array    $pictograms
 * @return	string   $buffer
 */
protected function pickup_sbm_sjis($buffer, $encoding, $pictograms) {
	$GLOBALS['pictograms'] = $pictograms; // pass $pictograms to eval scope of mb_ereg_replace/preg_replace
	$replaced = $this->base->encode->replace(
		"([\xf7\x40-\xf7\x5b]|[\xf7\x5d-\xf7\xfc]|[\xf9\x40-\xf9\x5b]|[\xf9\x5d-\xf9\xfc]|[\xfb\x40-\xfb\x5b]|[\xfb\x5d-\xfb\xfc])", 
		'isset($GLOBALS["pictograms"]["\1"]) ? 
		"<img localsrc=\"" . $GLOBALS["pictograms"]["\1"] . "\" />" : 
		"<img localsrc=\"se\" alt=\"" . "[0x" . bin2hex("\1") . "]\" />"', 
		$buffer, 'SJIS-win', 'e');
	if ($replaced) {
		$replaced = $this->base->encode->replace("\xf7\x5c", '<img localsrc="se11c" />', $replaced, 'SJIS-win');
		$replaced = $this->base->encode->replace("\xf9\x5c", '<img localsrc="se01c" />', $replaced, 'SJIS-win');
		$replaced = $this->base->encode->replace("\xfb\x5c", '<img localsrc="se41c" />', $replaced, 'SJIS-win');
	}
	return $replaced ? $replaced : $buffer;
}

// ===== End of class ====================
}

